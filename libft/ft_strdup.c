#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*ptr;
	size_t	size;
	int		i;

	size = ft_strlen(s1);
	ptr = (char *) malloc(size + 1);
	if (!ptr)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		ptr[i] = s1[i];
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
