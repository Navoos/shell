#include "libft.h"

int	ft_atoi(const char *str)
{
	unsigned int	i;
	long int		res;
	long int		neg;

	i = 0;
	neg = 1;
	if (!str)
		return (0);
	while (str[i] == '\t' || str[i] == '\n' || str[i] == '\v'
		|| str[i] == '\f' || str[i] == '\r' || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			neg = -1;
		i++;
	}
	res = 0;
	while (ft_isdigit(str[i]))
		res = (res * 10) + (str[i++] - '0');
	return (res * neg);
}
